package main;



public class Favor {
	

	
	public static void main(String[] args) {
	

		
		int[][] myNumber = {{1, 2, 3}, {4, 5, 6}}; //2D Array
		
		
		for(int x = 0; x < myNumber.length; x++) {
			
			for(int y = 0; y < myNumber[x].length; y++) {
				
				System.out.println(myNumber[x][y]);
			}
		}
		
		
		
	
	}
}
